package com.cibertec.appdocente.appdocentecibertec.Model;

public class Alumno {
    private  String codalum;
    private String nomalum;
    private String apealum;
    private int coddis;
    private String direccion;
    private String telfalum;
    private String correoalum;

    public String getCodalum() {
        return codalum;
    }

    public void setCodalum(String codalum) {
        this.codalum = codalum;
    }

    public String getNomalum() {
        return nomalum;
    }

    public void setNomalum(String nomalum) {
        this.nomalum = nomalum;
    }

    public String getApealum() {
        return apealum;
    }

    public void setApealum(String apealum) {
        this.apealum = apealum;
    }

    public int getCoddis() {
        return coddis;
    }

    public void setCoddis(int coddis) {
        this.coddis = coddis;
    }

    public String getDireccion() {
        return direccion;
    }

    public void setDireccion(String direccion) {
        this.direccion = direccion;
    }

    public String getTelfalum() {
        return telfalum;
    }

    public void setTelfalum(String telfalum) {
        this.telfalum = telfalum;
    }

    public String getCorreoalum() {
        return correoalum;
    }

    public void setCorreoalum(String correoalum) {
        this.correoalum = correoalum;
    }
}
