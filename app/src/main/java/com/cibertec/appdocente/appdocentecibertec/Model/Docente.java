package com.cibertec.appdocente.appdocentecibertec.Model;

public class Docente {
    private String coddocente;
    private String nomdocente;
    private String apedocente;
    private String telfdocente;
    private String nomdistrito;
    private String direcdocente;
    private String correodocente;
    //getter and setters
    public String getCoddocente() {
        return coddocente;
    }

    public void setCoddocente(String coddocente) {
        this.coddocente = coddocente;
    }

    public String getNomdocente() {
        return nomdocente;
    }

    public void setNomdocente(String nomdocente) {
        this.nomdocente = nomdocente;
    }

    public String getApedocente() {
        return apedocente;
    }

    public void setApedocente(String apedocente) {
        this.apedocente = apedocente;
    }

    public String getTelfdocente() {
        return telfdocente;
    }

    public void setTelfdocente(String telfdocente) {
        this.telfdocente = telfdocente;
    }

    public String getNomdistrito() {
        return nomdistrito;
    }

    public void setNomdistrito(String nomdistrito) {
        this.nomdistrito = nomdistrito;
    }

    public String getDirecdocente() {
        return direcdocente;
    }

    public void setDirecdocente(String direcdocente) {
        this.direcdocente = direcdocente;
    }

    public String getCorreodocente() {
        return correodocente;
    }

    public void setCorreodocente(String correodocente) {
        this.correodocente = correodocente;
    }
}
