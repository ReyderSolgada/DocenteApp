package com.cibertec.appdocente.appdocentecibertec.Model;

public class TemasCurso {
    private int codcurso;
    private int nrotema;
    private String destema;
    private String detsemana;

    public int getCodcurso() {
        return codcurso;
    }

    public void setCodcurso(int codcurso) {
        this.codcurso = codcurso;
    }

    public int getNrotema() {
        return nrotema;
    }

    public void setNrotema(int nrotema) {
        this.nrotema = nrotema;
    }

    public String getDestema() {
        return destema;
    }

    public void setDestema(String destema) {
        this.destema = destema;
    }

    public String getDetsemana() {
        return detsemana;
    }

    public void setDetsemana(String detsemana) {
        this.detsemana = detsemana;
    }
}
